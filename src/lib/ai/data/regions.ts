// 빠른 lookup용 헬퍼
export function getDistrictsByRegionId(regionId: string): { id: string; name: string }[] {
  const r = REGION_DATA.find((x) => x.id === regionId);
  return r?.districts ?? [];
}

export function getRegionName(regionId: string): string {
  return REGION_DATA.find((r) => r.id === regionId)?.name ?? '';
}

export function getDistrictName(regionId: string, districtId: string): string {
  return getDistrictsByRegionId(regionId).find((d) => d.id === districtId)?.name ?? '';
}

// NLU용: 시군구명 → { regionId, districtId } reverse lookup
export function findDistrictByName(name: string): { regionId: string; districtId: string } | null {
  for (const region of REGION_DATA) {
    for (const d of region.districts) {
      // 정확히 일치하거나 부분 매치 ("강남" → "강남구")
      if (d.name === name || d.name.startsWith(name) || name.startsWith(d.name)) {
        return { regionId: region.id, districtId: d.id };
      }
    }
  }
  return null;
}
